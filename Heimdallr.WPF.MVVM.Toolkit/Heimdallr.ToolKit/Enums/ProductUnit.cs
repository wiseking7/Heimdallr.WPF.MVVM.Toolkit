using Heimdallr.ToolKit.Attributes;
using System.ComponentModel;

namespace Heimdallr.ToolKit.Enums;
/// <summary>
/// 제품 단위를 나타내는 열거형입니다.
/// </summary>
public enum ProductUnit
{
  /// <summary>
  /// 각 제품 단위를 나타냅니다.
  /// </summary>
  [UnitInfo("낱개", 1)]
  [Description("각 제품 단위를 나타냅니다.")]
  Each,

  /// <summary>
  /// 제품 단위를 묶음으로 나타냅니다. 일반적으로 여러 개의 제품이 함께 판매되는 경우에 사용됩니다.
  /// </summary>
  [UnitInfo("팩", 3)]  // 기본 3개 묶음, 상황에 따라 변경 가능
  [Description("제품 단위를 묶음으로 나타냅니다. 일반적으로 여러 개의 제품이 함께 판매되는 경우에 사용됩니다.")]
  Pack,

  /// <summary>
  /// 제품 단위를 상자 단위로 나타냅니다. 일반적으로 포장된 제품이 상자에 들어 있는 경우에 사용됩니다.
  /// </summary>
  [UnitInfo("상자", 1)] // 개수 불확실할 경우 1로 표기
  [Description("제품 단위를 상자 단위로 나타냅니다. 일반적으로 포장된 제품이 상자에 들어 있는 경우에 사용됩니다.")]
  Box,

  /// <summary>
  /// 제품 단위를 묶음으로 나타냅니다. 일반적으로 여러 개의 제품이 함께 묶여 판매되는 경우에 사용됩니다.
  /// </summary>
  [UnitInfo("다스", 12)]  // 12개 묶음
  [Description("제품 단위를 묶음으로 나타냅니다. 일반적으로 여러 개의 제품이 함께 묶여 판매되는 경우에 사용됩니다.")]
  Dozen,

  /// <summary>
  /// 제품 단위를 그램 단위로 나타냅니다. 일반적으로 무게가 가벼운 제품에 사용됩니다.
  /// </summary>
  [UnitInfo("그램", 1)]
  [Description("제품 단위를 그램 단위로 나타냅니다. 일반적으로 무게가 가벼운 제품에 사용됩니다.")]
  Gram,

  /// <summary>
  /// 제품 단위를 킬로그램 단위로 나타냅니다. 일반적으로 무게가 무거운 제품에 사용됩니다.
  /// </summary>
  [UnitInfo("킬로그램", 1000)]  // 1kg = 1000g
  [Description("제품 단위를 킬로그램 단위로 나타냅니다. 일반적으로 무게가 무거운 제품에 사용됩니다.")]
  Kilogram,

  /// <summary>
  /// 제품 단위를 밀리리터 단위로 나타냅니다. 일반적으로 매우 가벼운 액체 제품에 사용됩니다.
  /// </summary>
  [UnitInfo("밀리리터", 1)]
  [Description("제품 단위를 밀리리터 단위로 나타냅니다. 일반적으로 매우 가벼운 액체 제품에 사용됩니다.")]
  Milliliter,

  /// <summary>
  /// 제품 단위를 리터 단위로 나타냅니다. 일반적으로 액체 제품의 용량을 측정하는 데 사용됩니다.
  /// </summary>
  [UnitInfo("리터", 1000)]  // 1L = 1000mL
  [Description("제품 단위를 리터 단위로 나타냅니다. 일반적으로 액체 제품의 용량을 측정하는 데 사용됩니다.")]
  Liter,

  /// <summary>
  /// 제품 단위를 미터 단위로 나타냅니다. 일반적으로 길이를 측정하는 데 사용됩니다.
  /// </summary>
  [UnitInfo("미터", 1)]
  [Description("제품 단위를 미터 단위로 나타냅니다. 일반적으로 길이를 측정하는 데 사용됩니다.")]
  Meter,

  /// <summary>
  /// 제품 단위를 제곱미터 단위로 나타냅니다. 일반적으로 면적을 측정하는 데 사용됩니다.
  /// </summary>
  [UnitInfo("제곱미터", 1)]
  [Description("제품 단위를 제곱미터 단위로 나타냅니다. 일반적으로 면적을 측정하는 데 사용됩니다.")]
  SquareMeter,

  /// <summary>
  /// 제품 단위를 세제곱미터 단위로 나타냅니다. 일반적으로 부피를 측정하는 데 사용됩니다.
  /// </summary>
  [UnitInfo("세제곱미터", 1)]
  [Description("제품 단위를 세제곱미터 단위로 나타냅니다. 일반적으로 부피를 측정하는 데 사용됩니다.")]
  CubicMeter,

  /// <summary>
  /// 제품 단위를 개별 조각으로 나타냅니다. 일반적으로 작은 제품이나 부품에 사용됩니다.
  /// </summary>
  [UnitInfo("조각", 1)]
  [Description("제품 단위를 개별 조각으로 나타냅니다. 일반적으로 작은 제품이나 부품에 사용됩니다.")]
  Piece,

  /// <summary>
  /// 제품 단위를 세트로 나타냅니다. 일반적으로 여러 개의 제품이 함께 구성된 경우에 사용됩니다.
  /// </summary>
  [UnitInfo("세트", 1)]
  [Description("제품 단위를 세트로 나타냅니다. 일반적으로 여러 개의 제품이 함께 구성된 경우에 사용됩니다.")]
  Set,

  /// <summary>
  /// 제품 단위를 롤 단위로 나타냅니다. 일반적으로 긴 형태의 제품에 사용됩니다.
  /// </summary>
  [UnitInfo("롤", 1)]
  [Description("제품 단위를 롤 단위로 나타냅니다. 일반적으로 긴 형태의 제품에 사용됩니다.")]
  Roll,

  /// <summary>
  /// 제품 단위를 묶음으로 나타냅니다. 일반적으로 여러 개의 제품이 함께 포장된 경우에 사용됩니다.
  /// </summary>
  [UnitInfo("묶음", 1)]
  [Description("제품 단위를 묶음으로 나타냅니다. 일반적으로 여러 개의 제품이 함께 포장된 경우에 사용됩니다.")]
  Bundle,

  /// <summary>
  /// 제품 단위를 병 단위로 나타냅니다. 일반적으로 액체 제품이 병에 담겨 있는 경우에 사용됩니다.
  /// </summary>
  [UnitInfo("병", 1)]
  [Description("제품 단위를 병 단위로 나타냅니다. 일반적으로 액체 제품이 병에 담겨 있는 경우에 사용됩니다.")]
  Bottle,

  /// <summary>
  /// 제품 단위를 봉지 단위로 나타냅니다. 일반적으로 작은 제품이나 식품이 봉지에 담겨 있는 경우에 사용됩니다.
  /// </summary>
  [UnitInfo("봉지", 1)]
  [Description("제품 단위를 Bag(봉지) 단위로 나타냅니다. 일반적으로 작은 제품이나 식품이 봉지에 담겨 있는 경우에 사용됩니다.")]
  Bag,

  /// <summary>
  /// 제품 단위를 캔 단위로 나타냅니다. 일반적으로 음료나 식품이 캔에 담겨 있는 경우에 사용됩니다.
  /// </summary>
  [UnitInfo("캔", 1)]
  [Description("제품 단위를 캔 단위로 나타냅니다. 일반적으로 음료나 식품이 캔에 담겨 있는 경우에 사용됩니다.")]
  Can,

  /// <summary>
  /// 제품 단위를 튜브 단위로 나타냅니다. 일반적으로 크림, 연고, 페인트 등이 튜브에 담겨 있는 경우에 사용됩니다.
  /// </summary>
  [UnitInfo("튜브", 1)]
  [Description("제품 단위를 튜브 단위로 나타냅니다. 일반적으로 크림, 연고, 페인트 등이 튜브에 담겨 있는 경우에 사용됩니다.")]
  Tube,

  /// <summary>
  /// 제품 단위를 시트 단위로 나타냅니다. 일반적으로 종이나 얇은 재료가 시트 형태로 제공되는 경우에 사용됩니다.
  /// </summary>
  [UnitInfo("시트", 1)]
  [Description("제품 단위를 시트 단위로 나타냅니다. 일반적으로 종이나 얇은 재료가 시트 형태로 제공되는 경우에 사용됩니다.")]
  Sheet,

  /// <summary>
  /// 제품 단위를 단위로 나타냅니다. 일반적으로 개별 제품을 나타내는 데 사용됩니다.
  /// </summary>
  [UnitInfo("단위", 1)]
  [Description("제품 단위를 단위로 나타냅니다. 일반적으로 개별 제품을 나타내는 데 사용됩니다.")]
  Unit,
}
