namespace Heimdallr.ToolKit.Indicators;

//[02]
public enum LoadingSpinnerType
{
  Bar,                      // 수평 바 형태의 로딩 애니메이션		
  Blocks,                   // 여러 개의 블록(사각형)을 사용하는 로딩 애니메이션		
  BouncingDot,              // 튕기는 점을 사용한 로딩 애니메이션		
  Cogs,                     // 톱니바퀴 모양을 사용한 로딩 애니메이션		
  Cupertino,                // Apple의 Cupertino 스타일을 사용하는 로딩 애니메이션		
  Dashes,                   // 대시 모양의 로딩 애니메이션		
  DotCircle,                // 원 모양의 점을 사용하는 로딩 애니메이션		
  DoubleBounce,             // 두 개의 점이 튕기는 로딩 애니메이션		
  Ellipse,                  // 타원형 애니메이션		
  Escalade,                 // Escalade 스타일의 로딩 애니메이션		
  FourDots,                 // 네 개의 점이 순차적으로 나타나는 애니메이션		
  Grid,                     // 격자 형태의 로딩 애니메이션		
  None,                     // 없음
  Piston,                   // 피스톤처럼 움직이는 애니메이션		
  Pulse,                    // '펄스' 스타일의 로딩 애니메이션 (확장 및 축소)		
  Ring,                     // 원 형태로 돌아가는 로딩 애니메이션		
  Swirl,                    // 나선형 형태로 회전하는 로딩 애니메이션		
  ThreeDots,                // 세 개의 점이 순차적으로 나타나는 애니메이션		
  Twist,                    // 트위스트 형태로 움직이는 로딩 애니메이션		
  Wave         							// 파도 모양의 애니메이션		
}
